import Foundation
import UIKit

extension UIViewController {
    func gsno(_ value: String?) -> String {
        return value ?? ""
    }
    
    func gino(_ value: Int?) -> Int {
        return value ?? 0
    }
}

extension NSObject {
    static var reuseIdentifier: String {
        return String(describing: self)
    }
}
